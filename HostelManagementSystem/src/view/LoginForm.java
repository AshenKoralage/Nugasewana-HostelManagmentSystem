        boolean isValidate = validateUserDetail(userName, password);
        boolean isCorrect = isCorrectUserDetail(userName, password);

//        System.out.println(isValidate + " : " + isCorrect);

        if (isValidate && isCorrect) {
            DashBoard f1 = new DashBoard();
            f1.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Please Check your User Name or Password Again");
            txtpassword.setText("");
        }
        
