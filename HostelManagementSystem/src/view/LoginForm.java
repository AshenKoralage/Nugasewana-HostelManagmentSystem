    private boolean validateUserDetail(String userName, String password) {

        if (userName == "" || password == null) {

            return false;
        } else {
            return true;
        }
    }

    private boolean isCorrectUserDetail(String userName, String password) {
      boolean isCorrect=false;
        try {
            Login login = new LoginController().systemUserLogin(userName, password);
           
            if (login == null) {
                isCorrect = false;
            }else{
                isCorrect =true;
            }
        } catch (Exception ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        return isCorrect;
    }

