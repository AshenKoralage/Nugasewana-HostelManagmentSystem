    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String typename = cmbroomtype.getSelectedItem().toString();
            String gender = null;
            if (btnmale.isSelected()) {
                gender = "male";
            } else {
                gender = "female";
            }
            ArrayList<ReservationDTO> getAllDetails = ReservationController.getAllDetails(typename, gender);
            dtm.setRowCount(0);
            for (ReservationDTO details : getAllDetails) {
                Object rowData[] = {details.getHID(), details.getRoom_no(), details.getCurrent_Borders_count(), details.getNo_of_beds(), details.getFloor_number(), details.getWidth(), details.getLength()};
                dtm.addRow(rowData);
                for (int i = 0; i < dtm.getRowCount(); i++) {
                    int noOfBeds = Integer.parseInt(dtm.getValueAt(i, 3).toString());
                    int currentBoders = Integer.parseInt(dtm.getValueAt(i, 2).toString());
                    int free = noOfBeds - currentBoders;
                    dtm.setValueAt(free, i, 7);

                }

            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ReservationForm1.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ReservationForm1.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

