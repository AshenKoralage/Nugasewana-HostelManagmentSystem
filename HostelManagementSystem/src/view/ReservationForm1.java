    private void loadAllroomTypes() {
        cmbroomtype.removeAll();
        try {
            ArrayList<TypeDTO> allTypes = TypeController.getAllTypes();
            for (TypeDTO allT : allTypes) {
                cmbroomtype.addItem(allT.getName());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ReservationForm1.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ReservationForm1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
